package com.youth.bean;
/**
 * 部门表
 * @author Administrator
 *
 */
public class VoteResult {
	private String depart;//部门
	
	private String basic;//基层单位
	
	private String satisfly;//满意
	
	private String litteSatisfly;//基本满意
	
	private String unsatisfly;//不满意
	
	private String tickets;//票数
	
	private String rank;//排名

	public String getDepart() {
		return depart;
	}

	public void setDepart(String depart) {
		this.depart = depart;
	}

	public String getBasic() {
		return basic;
	}

	public void setBasic(String basic) {
		this.basic = basic;
	}

	public String getSatisfly() {
		return satisfly;
	}

	public void setSatisfly(String satisfly) {
		this.satisfly = satisfly;
	}

	public String getLitteSatisfly() {
		return litteSatisfly;
	}

	public void setLitteSatisfly(String litteSatisfly) {
		this.litteSatisfly = litteSatisfly;
	}

	public String getUnsatisfly() {
		return unsatisfly;
	}

	public void setUnsatisfly(String unsatisfly) {
		this.unsatisfly = unsatisfly;
	}

	public String getTickets() {
		return tickets;
	}

	public void setTickets(String tickets) {
		this.tickets = tickets;
	}

	public String getRank() {
		return rank;
	}

	public void setRank(String rank) {
		this.rank = rank;
	}

	
	
}
